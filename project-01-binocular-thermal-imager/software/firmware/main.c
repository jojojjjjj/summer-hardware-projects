/**
 * @brief 错误处理函数 | Error handler function
 *
 * 通过 LED 闪烁次数指示错误类型
 * Indicate error type by LED blink count
 *
 * @param err_code 错误代码 | Error code
 */
static void Error_Handler(uint8_t err_code)
{
    error_state = err_code;

    /* 无限循环，LED 闪烁指示错误类型 | Infinite loop, LED blinks to indicate error */
    while (1)
    {
        Blink_LED(err_code, 200);
        HAL_Delay(1000);
    }
}

/**
 * @brief LED 闪烁函数 | LED blink function
 *
 * @param count    闪烁次数 | Number of blinks
 * @param period_ms 每次闪烁周期 (ms) | Blink period per cycle (ms)
 */
static void Blink_LED(uint8_t count, uint32_t period_ms)
{
    for (uint8_t i = 0; i < count; i++)
    {
        HAL_GPIO_WritePin(LED_PORT, LED_PIN, GPIO_PIN_RESET);  /* LED 亮 | On */
        HAL_Delay(period_ms / 2);
        HAL_GPIO_WritePin(LED_PORT, LED_PIN, GPIO_PIN_SET);    /* LED 灭 | Off */
        HAL_Delay(period_ms / 2);
    }
}

