/**
 * @brief 主函数入口 | Main entry point
 *
 * 初始化流程 | Initialization sequence:
 *   1. HAL 库初始化 | HAL library init
 *   2. 系统时钟配置 | System clock config
 *   3. GPIO 初始化 (LED) | GPIO init (LED)
 *   4. I2C 初始化 | I2C init
 *   5. USB CDC 初始化 | USB CDC init
 *   6. MLX90640 传感器初始化 | MLX90640 sensor init
 *   7. 进入主循环 | Enter main loop
 */
int main(void)
{
    /* ---- 第一步: HAL 库初始化 ---- */
    /* Step 1: HAL library initialization */
    HAL_Init();

    /* ---- 第二步: 配置系统时钟 ---- */
    /* Step 2: Configure system clock */
    /* STM32F411: 使用 HSE (外部晶振 8MHz) 通过 PLL 倍频到 100MHz */
    /* STM32F411: Use HSE (external 8MHz crystal), PLL to 100MHz */
    SystemClock_Config();

    /* ---- 第三步: GPIO 初始化 ---- */
    /* Step 3: GPIO initialization (LED on PC13) */
    GPIO_Init();

    /* 启动指示: 快闪 3 次 | Startup indicator: blink 3 times fast */
    Blink_LED(3, 100);

    /* ---- 第四步: I2C 初始化 ---- */
    /* Step 4: I2C initialization */
    I2C1_Init();

    /* ---- 第五步: USB CDC 初始化 ---- */
    /* Step 5: USB CDC initialization */
    /* 初始化 USB 设备并注册 CDC 类 | Init USB device and register CDC class */
    if (USBD_Init(&hUsbDeviceFS, &FS_Desc, DEVICE_FS) != USBD_OK) {
        Error_Handler(ERR_USB_INIT);
    }
    if (USBD_RegisterClass(&hUsbDeviceFS, &USBD_CDC) != USBD_OK) {
        Error_Handler(ERR_USB_INIT);
    }
    if (USBD_CDC_RegisterInterface(&hUsbDeviceFS, &USBD_Interface_fops_FS) != USBD_OK) {
        Error_Handler(ERR_USB_INIT);
    }
    if (USBD_Start(&hUsbDeviceFS) != USBD_OK) {
        Error_Handler(ERR_USB_INIT);
    }

    /* 等待 USB 枚举完成 | Wait for USB enumeration */
    HAL_Delay(500);
    usb_ready = 1;

    /* ---- 第六步: MLX90640 传感器初始化 ---- */
    /* Step 6: MLX90640 sensor initialization */
    if (MLX90640_Init(&hi2c1, MLX90640_I2C_ADDR) != MLX90640_OK) {
        Error_Handler(ERR_SENSOR_INIT);
    }
    sensor_ready = 1;

    /* 初始化成功指示: 慢闪 2 次 | Init success: blink 2 times slow */
    Blink_LED(2, 300);

    /* ---- 第七步: 主循环 ---- */
    /* Step 7: Main loop */
    while (1)
    {
        /* 检查系统和传感器状态 | Check system and sensor status */
        if (!sensor_ready || !usb_ready) {
            HAL_Delay(MAIN_LOOP_DELAY_MS);
            continue;
        }

        /* 读取一帧热成像数据 | Read one frame of thermal data */
        /* MLX90640 需要读取两个子页才能组成完整的一帧 */
        /* MLX90640 requires reading two sub-pages for a complete frame */
        if (MLX90640_ReadFrame(&hi2c1, thermal_frame) == MLX90640_OK)
        {
            /* 打包数据并通过 USB CDC 发送 */
            /* Package data and send via USB CDC */
            USB_CDC_SendThermalFrame(thermal_frame, frame_count);

            /* 更新帧计数器 | Update frame counter */
            frame_count++;

            /* LED 闪烁指示数据传输 | LED blink to indicate data transfer */
            HAL_GPIO_TogglePin(LED_PORT, LED_PIN);
        }
        else
        {
            /* 帧读取错误 | Frame read error */
            error_state = ERR_FRAME_READ;
        }

        /* 主循环延时 | Main loop delay */
        HAL_Delay(MAIN_LOOP_DELAY_MS);
    }
}

